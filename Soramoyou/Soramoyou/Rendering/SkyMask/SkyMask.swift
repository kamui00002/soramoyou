import CoreImage

/// マスク生成の品質モード
///
/// なぜ品質モードを分けるか:
/// - プレビュー用途（編集画面でのスライダー操作など、リアルタイム性が求められる場面）は
///   解析グリッドを小さくして CPU ループのコストを下げ、応答性を優先する。
/// - 書き出し用途（最終的な画像を保存・投稿する場面）は解析グリッドを大きくし、
///   境界の精度を優先する。
enum SkyMaskQuality {
    /// 解析グリッド長辺 192px・高速（UIプレビュー用）
    case preview
    /// 解析グリッド長辺 384px・高精度（書き出し用）
    case export
}

/// 生成された空マスクと付帯情報
///
/// マスクそのものだけでなく、そのマスクが「どれくらい空を捉えているか」「どれくらい確信を持てるか」を
/// 呼び出し側（例: 空だけを対象にした編集処理）が判断できるよう、統計値も併せて返す。
struct SkyMask {
    /// グレースケール CIImage。1.0=空 / 0.0=非空 / 中間値=境界ソフトエッジ。
    /// extent は入力画像の extent と厳密に一致する（呼び出し側で追加のクロップ調整が不要なように保証する）。
    let mask: CIImage
    /// 空が画面に占める割合 0...1（平滑化前のスコアグリッドの平均）
    ///
    /// 平滑化前の値を使う理由: ガウシアンブラーやコントラスト強調は「見た目のエッジ」を整えるための
    /// 後処理であり、統計値としての「空らしさの総量」を歪めたくないため。
    let skyCoverage: Double
    /// マスクの自己申告信頼度 0...1（スコアが0.2以下または0.8以上の「白黒はっきりした」画素の割合）
    ///
    /// 0.2〜0.8 のグレーゾーンが多い（＝白黒つけにくい判定が多い）画像ほど信頼度は下がる。
    /// v1 はヒューリスティック（色相・彩度・明度・縦位置の事前確率）による近似でしかないため、
    /// 呼び出し側がこの信頼度を見て「マスクを弱く適用する」等の判断をできるようにする。
    let confidence: Double
}

/// SkyMask 生成時に発生しうるエラー
enum SkyMaskError: Error {
    /// extent が空・無限・CGImage化不能などの不正な入力
    case invalidInput
    /// フィルタグラフ構築や画素読み出しの失敗
    case generationFailed
}

/// 空マスクの一元供給層。
///
/// なぜ protocol にするか:
/// v1 はヒューリスティック（色分析＋縦位置の事前確率）で実装するが、
/// 将来的に CoreML ベースのセグメンテーションモデルへ差し替える可能性がある。
/// 呼び出し側をこの protocol にのみ依存させることで、バックエンドの実装を
/// 意識せずに差し替えられるようにする。
protocol SkyMaskProviding {
    /// 入力画像から空マスクを生成する
    /// - Parameters:
    ///   - image: 向き正規化済み（.up 焼き込み済み）の CIImage を渡すこと（provider は orientation を扱わない）
    ///   - quality: マスク生成の品質モード
    /// - Returns: 生成された SkyMask
    func makeSkyMask(for image: CIImage, quality: SkyMaskQuality) async throws -> SkyMask
}
