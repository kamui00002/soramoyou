import CoreGraphics

/// Living Sky v1 のユーザー可変パラメータ（設計書§6のテーブルに対応）。
///
/// すべて `var` で保持し、プレビュー中のスライダー操作でそのまま更新できるようにする。
/// `Equatable` は SwiftUI 側での変更検知（`onChange` 等）に使うため付与している。
struct LivingSkyParameters: Equatable {
    /// 風向き（度数）。0=右向き、反時計回りに増加する。
    var windAngleDegrees: Double = 0

    /// 速さ（≒変位振幅の強さ）。0.1〜1.0 の範囲を想定。
    /// 「自然さ最優先」の方針（設計書§6）から既定値はゆっくり寄りの 0.5。
    var speed: Double = 0.5

    /// 光のゆらぎの強さ 0...0.10。0 で無効。
    ///
    /// v7 で既定値を 0.05 → 0.012 へ縮小した。出典: docs/research/living-sky-research-2026-07.md
    /// 「推奨パラメータ」lightAmp（初期値±1.2%・安全範囲±0.5%〜±2.5%）。v7 は「生きてる感」の
    /// 主役を周期モーフと控えめな光のゆらぎに持たせる方針（LivingSky.metal v7 ブロックのコメント
    /// 参照）のため、UI スライダーの range も 0...0.025 へ縮小している
    /// （`LivingSkyPreviewView.controlsArea` 参照）。engine 側の kernel 引数クランプ
    /// （`min(max(shimmerAmount, 0), 0.1)`）はスライダー range より広い安全上限のため据え置き。
    var shimmerAmount: Double = 0.012

    /// ループ長 T（秒）。4〜10 秒を想定。長いほど継ぎ目・クロスフェードの「呼吸」が目立たない
    /// （設計書§2.1「既知のトレードオフ」参照）。
    ///
    /// v2 で 8.0 → 6.0 に短縮、v4（窓クロスフェード方式）はフェードが各所で T/2 に1回しか
    /// 起きないため T を長く取るほどフェード頻度が下がり自然に見えると判断し 8.0 に戻した。
    /// v5（変位ブレンド）へ刷新した際、`maxDisplacementPx` の係数を 0.08→0.05 に下げた
    /// （巻き戻し幅は変位振幅に比例するため）。これにより体感速度が下がるため、6.0 秒へ
    /// 短縮してドリフトの速度感を補償する（px/s は概ね 0.08/8 秒 ≒ 0.05/6 秒 × 1.25 でほぼ
    /// 維持——完全一致ではなく意図的にやや速め寄りに残している）。range 4...10 は据置。
    /// v3「軌道うねり」を選ぶ場合も同じ値を使う。
    ///
    /// v6（色クロスフェード復活＋エッジ接線偏向）への刷新時も、変位振幅の係数 0.05
    /// （当時の関数名は `maxDisplacementPx`。v7 で `driftAmplitudePx` に改名・短辺基準へ変更
    /// 済み——下のコメント参照）・この 6.0 秒は共に v5 からそのまま据え置いた。
    ///
    /// v7（レポート準拠ハイブリッド。出典: docs/research/living-sky-research-2026-07.md）でも
    /// 6.0 秒を据え置いた。レポートは初期値 4.5s を推奨するが、v7 は既存の体感速度
    /// （`driftAmplitudePx` の振幅設計）をそのまま維持する方針のため変更していない。
    var loopDuration: Double = 6.0

    /// 動きモデルの切替（0=ドリフト[v6] / 1=軌道うねり[v3]）。既定は 0（v6ドリフト）。
    ///
    /// ユーザー実機評価（2026-07-15）: v3「軌道うねり」は「ちょっと気持ち悪い」、
    /// v2「クロスフェードドリフト」は「雲が多重に見えて錯視みたい」——ただしどちらかと言えば
    /// ドリフト系を支持、との判定を受け、v2 の欠陥（三相正規化ブレンドが静止時点でも常に
    /// 3コピーが重なって見える「多重露光」構造）を Valve の flow map 定石
    /// （窓クロスフェード＋画素ごと位相オフセット）で解消した v4 を新既定にした。
    /// その後 v4 も TestFlight 実機評価（2026-07-16）で「輪郭の鋭い雲だと分身が見える」と
    /// 最終NGを受けた（クロスフェード窓の間は2コピーの**色**が重なって合成されるため）。
    /// 色のブレンドをやめ「変位（サンプル位置）のブレンド」に変えた v5 へ刷新したが、v5 は
    /// 窓の間にサンプル位置が新位相へ滑り戻る「局所的な巻き戻し」（順方向の約2.3倍速）が
    /// ユーザー実機評価（2026-07-17）で「すごい気持ち悪い」と最終NGを受けた。v6 は発想を
    /// 変え、v4 の色クロスフェード（動きの性格としては承認済み）を復活させた上で、輝度勾配で
    /// 検出した輪郭の向きへフロー方向を曲げる「エッジ接線偏向」を追加し、分身の発生源そのもの
    /// （輪郭を横切る方向の変位）を抑える方式にして、これを新既定にした（motionModel=0 の
    /// まま実装のみ差し替え。新しい motionModel 値は無い）。
    ///
    /// - v6（ドリフト・新既定）: v4 方式の色クロスフェード（位相2枚のどちらの色を使うかを
    ///   重み wA でブレンド）に、輝度勾配ベースのエッジ接線偏向を追加した方式。輪郭が強い
    ///   場所ではフロー方向を輪郭の接線方向へ曲げ、ずれたコピーが元の輪郭上に重なるように
    ///   することで二重線（分身）を抑える。のっぺりした空の内部（勾配が弱い領域）は偏向が
    ///   かからず v4 と同じ挙動のまま風向きに流れる。
    /// - v3（軌道うねり・比較用に残置）: クロスフェードを廃し、各画素のサンプル点が風向きに
    ///   長軸を向けた閉軌道（楕円）を周回する方式。分身は原理的にゼロだがユーザー評価は
    ///   「気持ち悪い」だったため既定からは外した。
    var motionModel: Int = 0

    /// 空のドリフト振幅（px）を画像短辺から導出する。
    ///
    /// v1〜v6 は画像幅基準の `maxDisplacementPx(imageWidth:)` という名前・式だったが、v7
    /// （レポート準拠ハイブリッド。出典: docs/research/living-sky-research-2026-07.md
    /// 「推奨パラメータ」driftAmp）で**短辺基準**へ改名・変更した。振幅は「絶対値より画像短辺比を
    /// 優先すべき」というレポートの指針、および v4〜v6 共通の失敗原因が「振幅過大」（旧係数0.05＝
    /// 短辺の5%幅/ループはレポート推奨の4〜8倍）だったという振り返り（LivingSky.metal の
    /// v7 導入コメント参照）に基づく。
    ///
    /// `driftPx = shortSide × 0.008 × (speed / 0.5)`。既定 speed=0.5 で短辺の**0.8%**幅
    /// （レポートの driftAmp 初期値と一致）、range 0.1〜1.0 で短辺の**0.16%〜1.6%**
    /// （レポート安全範囲0.4%〜1.8%にほぼ収まる）。旧係数（v1〜v6: 画像幅の5〜8%相当）から
    /// 大幅に縮小しているため、分身の見た目の幅も知覚限界（概ね3px相当）以下に収まる設計。
    /// - Parameter shortSide: ワーキング座標系での画像短辺（px）＝ `min(width, height)`
    /// - Returns: ドリフト振幅（px）
    func driftAmplitudePx(shortSide: CGFloat) -> CGFloat {
        shortSide * 0.008 * (CGFloat(speed) / 0.5)
    }
}
